def match_and_apply(data):
    events = data["events"]
    updated = 0

    for country, keyword, period, actual, forecast, previous in HISTORICAL:
        for event in events:
            # 匹配条件：同国家 + 指标名包含关键词 + 数据期间包含目标月份
            if (event["country"] == country and
                keyword in event.get("indicator", "") and
                period in event.get("period", "") and
                event.get("actual") is None):  # 已有实际值则不覆盖
                
                event["actual"] = actual
                event["forecast"] = forecast
                event["previous"] = previous
                event["status"] = "released"
                updated += 1
                break  # 只匹配第一个

    return updated


with open(CALENDAR_FILE, "r", encoding="utf-8") as f:
    data = json.load(f)

updated = match_and_apply(data)

# 统计
from datetime import date
today_str = date.today().strftime("%Y-%m-%d")
released = [e for e in data["events"] if e.get("status") == "released"]
upcoming = [e for e in data["events"] if e["release_date"] >= today_str]
with_fc = [e for e in upcoming if e.get("forecast") is not None]

data["meta"]["released_events"] = len(released)
data["meta"]["upcoming_with_forecast"] = len(with_fc)
data["meta"]["generated_at"] = f"{date.today().strftime('%Y-%m-%d')} (fixed)"

with open(CALENDAR_FILE, "w", encoding="utf-8") as f:
    json.dump(data, f, ensure_ascii=False, indent=2)

# JS
js = "// Auto-generated\nwindow.CALENDAR_DATA = " + json.dumps(data, ensure_ascii=False) + ";"
with open(JS_FILE, "w", encoding="utf-8") as f:
    f.write(js)

print(f"Fixed and applied {updated} historical data entries via fuzzy matching")
print(f"Released events: {len(released)}")
print(f"Upcoming with forecast: {len(with_fc)}")
